/**
 * Narrative Generator - Data storytelling for analytics
 *
 * Transforms raw metrics into natural language narratives
 * that merchants can easily understand.
 */

import type { HealthScore, AnalyticsInsight } from "./insight-engine.server";
import type { ComparisonResult } from "./comparison.server";

// ============================================================================
// Types
// ============================================================================

export type NarrativeTone = 'professional' | 'friendly' | 'urgent';
export type NarrativeLength = 'brief' | 'detailed';

export interface NarrativeConfig {
  tone: NarrativeTone;
  length: NarrativeLength;
  includeActions: boolean;
  includeComparisons: boolean;
}

export interface MetricNarrative {
  metric: string;
  summary: string;
  explanation: string;
  context: string;
  trend: string;
  recommendation?: string;
}

export interface ExecutiveSummary {
  performance: string;
  highlight: string;
  action: string;
  period: string;
}

export interface TrendNarrative {
  metric: string;
  description: string;
  direction: 'up' | 'down' | 'flat';
  keyPoints: string[];
  notableEvents?: string[];
}

export interface ChartNarrative {
  title: string;
  description: string;
  insights: string[];
  callout?: string;
}

// ============================================================================
// Default Configuration
// ============================================================================

const DEFAULT_CONFIG: NarrativeConfig = {
  tone: 'professional',
  length: 'brief',
  includeActions: true,
  includeComparisons: true,
};

// ============================================================================
// Metric Explanations
// ============================================================================

